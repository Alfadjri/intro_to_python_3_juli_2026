x = 3
y = 2

# Penjumlahan
hasil_penjumlahan = x + y
print(f"Hasil penjumlahan {x} + {y} = {hasil_penjumlahan}")
# Pengurangan
hasil_pengurangan = x - y
print(f"Hasil pengurangan {x} - {y} = {hasil_pengurangan}")
# Perkalian
hasil_perkalian = x * y
print(f"Hasil perkalian {x} * {y} = {hasil_perkalian}")
# Pembagian
hasil_pembagian = x / y
print(f"Hasil pembagian {x} / {y} = {hasil_pembagian}")
# Modulus
hasil_modulus = x % y
print(f"Hasil modulus {x} % {y} = {hasil_modulus}")
# Eksponen
hasil_eksponen = x ** y
print(f"Hasil eksponen {x} ** {y} = {hasil_eksponen}")
# Floor Division
hasil_floor_division = x // y
print(f"Hasil floor division {x} // {y} = {hasil_floor_division}")
# Increment
# x = x + 1 
x += 1
print(f"Hasil increment x += 1 = {x}")
# Decrement
y -= 1
print(f"Hasil decrement y -= 1 = {y}")